// Let's say we have a constant storing a string
const name = 'Bindi';

// And a variable storing a number 
let age = 22;

// And an object
let wildlifeWarrior = {
  name: name,
  age: age,
  gender: 'f',
};

// This will print 'Hello, world!' to console
console.log('Hello, world!');

// Additionally, you can print any value
console.log(name); // Prints 'Bindi'
console.log('name:', name); // Prints 'name: Bindi'
console.log('age: ' + age); // Prints 'age: 22'

// This will print an object as a string
console.log('Person:', wildlifeWarrior);
// Person: { name: 'Bindi', age: 22, gender: 'f' }
